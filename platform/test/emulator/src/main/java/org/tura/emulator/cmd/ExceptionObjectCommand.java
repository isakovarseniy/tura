package org.tura.emulator.cmd;

public class ExceptionObjectCommand implements EmulatorCommand{

	private String code;
	private String message;
	
	public ExceptionObjectCommand() {
	}

	
	public ExceptionObjectCommand(String code,String message ) {
		this.code = code;
		this.message = message;
	}
	
	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	@Override
	public Object execute() throws Exception {
		throw new EmulatorException(code, message);
	}


	@Override
	public String stringPayload() {
		return code+" = "+ message;
	}

}
