package org.elsoft.platform.metamodel.objects.command.form.datasource;

import javax.persistence.Column;
import javax.persistence.DiscriminatorValue;
import javax.persistence.Entity;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

import org.elsoft.platform.metamodel.objects.command.CommandDAO;

@Entity(name = "DefaultSearch")
@Table(name = "defaultsearch")
@DiscriminatorValue("DEFAULTSEARCH")
@XmlRootElement
public class CreateDefaultSearchDAO extends CommandDAO{

	@Column(name = "COLUMN_NAME")
	private String columnName;

	@Column(name = "COMPARATOR")
	private String comparator;

	@Column(name = "VALUE")
	private String value;
	
	@Column(name = "TYPE_NAME")
	private String typeName;

	@Column(name = "EXPRESSION")
	private String expression;



	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getExpression() {
		return expression;
	}

	public void setExpression(String expression) {
		this.expression = expression;
	}

	public String getColumnName() {
		return columnName;
	}

	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}

	public String getComparator() {
		return comparator;
	}

	public void setComparator(String comparator) {
		this.comparator = comparator;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

}
