package org.tura.platform.hr.init.datastructure;

import java.util.List;

public class Department {
	private Long departmentId;
	private String departmentIName;
	private Location locaton;
	private List<Telephone> telephones;
	private List <Employee> employees;
	
	public Long getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(Long departmentId) {
		this.departmentId = departmentId;
	}
	public String getDepartmentIName() {
		return departmentIName;
	}
	public void setDepartmentIName(String departmentIName) {
		this.departmentIName = departmentIName;
	}
	public Location getLocaton() {
		return locaton;
	}
	public void setLocaton(Location locaton) {
		this.locaton = locaton;
	}
	public List<Telephone> getTelephones() {
		return telephones;
	}
	public void setTelephones(List<Telephone> telephones) {
		this.telephones = telephones;
	}
	public List<Employee> getEmployees() {
		return employees;
	}
	public void setEmployees(List<Employee> employees) {
		this.employees = employees;
	}
	
	
	
}
