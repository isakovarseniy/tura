package org.tura.platform.datacontrol.commons;

public class OrderCriteria {
	private String name;
	private String order;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getOrder() {
		return order;
	}

	public void setOrder(String order) {
		this.order = order;
	}

}
