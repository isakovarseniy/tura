package org.tura.platform.datacontrol.command.base;

import java.io.Serializable;

public class CallParameter implements Serializable{
	private static final long serialVersionUID = 3434598650638358880L;
	private String name;
	private Object value;
	private Class<?> clazz;
	private String expression;
	private Object obj;

	public CallParameter(String name, String expression , Class<?> clazz){
		this.name = name;
		this.expression = expression;
		this.clazz = clazz;
	}
	
	public CallParameter(){
		
	}
	
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

	public String getExpression() {
		return expression;
	}

	public void setExpression(String expression) {
		this.expression = expression;
	}

	public Class<?> getClazz() {
		return clazz;
	}

	public void setClazz(Class<?> clazz) {
		this.clazz = clazz;
	}

	/**
	 * @return the obj
	 */
	public Object getObj() {
		return obj;
	}

	/**
	 * @param obj
	 *            the obj to set
	 */
	public void setObj(Object obj) {
		this.obj = obj;
	}

}